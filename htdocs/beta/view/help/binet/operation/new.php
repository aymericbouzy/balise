<h1>Sur cette page, tu peux créer de nouvelles opérations</h1>
<p>Les seuls champs obligatoires sont la description et le montant.
    Mais plus tu mettras d'informations, plus ça te sera utile !<br>

    Après avoir créer l'opération, il te faut la relier à tes lignes budgétaires.
    Tu peux affecter une opération à plusieurs lignes : le montant te restant à attribuer est indiqué en bas!<br>

    C'est comme ça que tu débloques tes subventions : il suffit que que tu assignes ton opération à une ligne subventionnée.
    N'oublie pas après d'aller apporter la facture à la Kès pour te faire rembourser !<br>

    Si tu n'as pas la ligne qui te convient, tu peux aller la créer, l'opération en attente ira dans l'onglet validation et tu pourras le faire plus tard.
</p>
