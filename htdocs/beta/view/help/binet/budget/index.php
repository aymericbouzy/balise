<h1>Tu es ici sur la trésorerie et sur l'historique de tes opérations.</h1>

<p>Grâce au bouton en haut à droite Budget/opérations, tu peux passer de l'un à l'autre.<br>

    Pour consulter les différents états de ton binet, tu peux naviguer via le menu situé à gauche. S’il y a numéro à côté de validation, c’est que tu à des actions à valider !<br>

    Avant toute chose, il te faudra créer ton budget : va voir dans le bouton "plus" !<br>
    Toutes les actions que tu peux effectuer sont dans ce bouton !<br>

    Après avoir créé ton budget, tu pourras créer des opérations (des dépenses ou rentrées d'argent effectives).<br>
    Tu pourras également demander des subventions, toujours via le bouton "plus"!<br>
    Pour utiliser ses subventions, cela se fait lors de la création d'une opération, tu seras guidé pour ça.<br>

    N'hésite pas à cliquer si tu veux avoir des détails sur tel ou tel ligne du tableau, c'est interactif !<br>
		<strong>Tu peux exporter ces tableaux avec un simple copier coller en sélectionnant avec ton curseur l'ensemble des données</strong>
</p>
